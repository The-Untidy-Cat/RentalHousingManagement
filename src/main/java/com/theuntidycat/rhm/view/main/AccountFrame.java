    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        if ((String.valueOf(inpPassword.getPassword()) == null ? String.valueOf(inpRetypePassword.getPassword()) == null : String.valueOf(inpPassword.getPassword()).equals(String.valueOf(inpRetypePassword.getPassword()))) && !"".equals(String.valueOf(inpPassword.getPassword()))) {
            if (accCtrl.changePassword(user.id, String.valueOf(inpPassword.getPassword()))) {
                JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu không thành công", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mật khẩu không khớp", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btSubmitActionPerformed

